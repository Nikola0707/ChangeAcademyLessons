// Declare a variable named 'fahrenheit' and assign it the value 32
// Declare a variable named 'celsius' to store the temperature convereted from Fahrenheit to Celsius

let fahrenheit = 32;

// Fahrenheit to Celsius
let celsius = ((fahrenheit - 32) * 5) / 9;
console.log("celsius", celsius);

// Fahrenheit to Kelvin
// Declare a variable named 'kelvin' to store the temperature converted from Fahrenheit to Kelbin

let kelvin = ((fahrenheit + 459.67) * 5) / 9;
